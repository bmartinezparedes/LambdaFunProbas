class funcionTipo {
    /**
     * Declaracion Function Type
     *
     * AX: argumentos de la funcion
     * R: tipo de dato que retorna
     *
     *   (A1,A2, ..., An) -> R
     *
     * Ejemplos:
     *
     *   (String, String) -> String
     *   Dos String como argumento y devuelve String
     *
     *   (Init) -> Unit
     *   Un Entero como argumento y devuleve nada en particular
     *
     *   () -> Double
     *   No tiene parametros y devuelve un Double
     */
}